        <h3>Stressad?</h3>
        <div class="column col-lg-6 banner-text">
          <p>Idag är vi beroende av snabbhet. Allt ska gå snabb. Om vi så beställer ett paket från ett annat land så vill vi att det kommer så fort som möjligt.</p>
          <p>Det är likadant med webbsidor. Laddas den inte tillräckligt snabbt så kommer användarna lämna dem på grund av frustration. Det finns en (om än gammal) undersökning som gjorts vid <strong><a href="http://www.metro.se/artikel/livsfarligt-med-tr%C3%B6g-hemsida-xr">Social Issues Research Center i Oxford</a></strong> med 2 500 användare som testats hur reaktionerna blir vid just långsamma webbplatser. Resultaten är ganska "roliga", för jag kan hålla med om vissa punkter som nämns där.</p>
          <p>Jag kommer att lösa det problemet med hjälp av att optimera alla bilder så att dem tar upp mindre plats på servern, minifiera alla koder som jag skrivit för att filen ska bli så liten som möjligt. Plus en hel del andra metoder. Med alla otimeringar jag gjort så kommer er sida att inte vara långsam längre och du kommer troligtvis få en högre ranking på Googles sökmotor.</p>
