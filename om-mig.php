          <h3>Lite om mig</h3>
          <div class="line"></div>
          <p>Hej, mitt namn är Lars Jönsson och jag har sysslat med webbutveckling sedan cirka ett år tillbaka nu. Vägen hit har varit den mest givande jag varit med om. Att få hålla på med det jag älskar är helt underbart. Min erfarenhet är inte lång men med min nyfikenhet och passion gör mig till en ivrig person som alltid strävar att lära sig det senaste och mest uppdaterade tekniken inom webbutveckling. Om jag får ett projekt av en kund som innebär mycket jobb av mig, så kan det ta mera tid än andra mera erfarna utvecklare. Men jag kommer lära mig på vägen, garanterat. Så till nästa projekt så kommer det att gå snabbare.
          <br>
          <br>
          Jag är just i startgroparna till att starta som <strong>frilansande webbutvecklare.</strong> Jag hoppas att hålla på med det här på heltid. Det jag kan hjälpa Er med är att skapa <strong>banners</strong> och så klart det mesta inom webben. I vissa fall så kan jag även hjälpa er med att ta fram en ny <strong>logga</strong> till ert företag eller förening. Jag är öppen för förslag av olika projekt av er.
          <br>
          <br>
          <a href="kontakt">Kontakta mig</a> med era förslag eller problem ni vill att jag ska hjälpa er med. Jag hör av mig samma dag.
